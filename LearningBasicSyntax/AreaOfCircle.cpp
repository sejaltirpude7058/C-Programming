//To find Area of circle
#include<iostream>
using namespace std;
int main(){
//taking radius input from user
   float radius;
   cout<<"Enter Radius: ";
   cin>>radius;
   const float pii = 3.1415;
   float area = pii*radius*radius;
   cout<<"The area of circle is "<<area;
   cout<<"The area of circle is "<<area;
}